// Tipos de dados 

// string
const name = 'João Paulo';
console.log(name);
console.log(typeof name);

// number
const shirtsQty = 10;
console.log(shirtsQty);
console.log(typeof shirtsQty);

// boolean
const isApproved = false; // true ou false
console.log(isApproved);
console.log(typeof isApproved);

// null 
let surname = null;
console.log(surname);
console.log(typeof surname); // object 

surname = 'João Paulo';
console.log(surname);
console.log(typeof surname);

// undefined
let age;
console.log(age);
console.log(typeof age);

age = 24;
console.log(age);
console.log(typeof age);

// array
const languages = ['Javascript', 'PHP', 'Java', 'C#'];
console.log(languages);
console.log(typeof languages);

// object 
const user = {email: 'jpcontato@gmail.com', password: 'teste123', age: 25}; // object literals 
console.log(user);
console.log(typeof user);